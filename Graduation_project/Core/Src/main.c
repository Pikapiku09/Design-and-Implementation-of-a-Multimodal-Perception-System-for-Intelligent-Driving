/**
 * @brief  諷秶MG996R嗆儂蛌雄善硌隅褒僅
 * @param  htim: 隅奀け曆梟硌渀 (瞰⺼ &htim2)
 * @param  channel: 隅奀け籵耋 (瞰⺼ TIM_CHANNEL_1)
 * @param  angle: 醴梓褒僅 (0 ~ 180僅)
 * @retval 拸
 */
void Servo_SetAngle(TIM_HandleTypeDef *htim, uint32_t channel, uint8_t angle)
{
    // 癹秶褒僅毓峓ㄛ滅砦閉堤儂迮癹弇
    if(angle > 180) angle = 180;

    // 數呾PWM闕遵硉
    // 0僅 = 0.5ms (500跺數杅硉)
    // 180僅 = 2.5ms (2500跺數杅硉)
    // 數呾鼠宒: Pulse = 500 + (angle * 2000 / 180)
    uint16_t pulse_width = 500 + (uint16_t)((angle * 2000) / 180);

    // 扢离掀誕敵湔け硉
    __HAL_TIM_SET_COMPARE(htim, channel, pulse_width);
}

